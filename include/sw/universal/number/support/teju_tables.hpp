// Multiplier entry: {lower, upper} limbs of a 2N-bit multiplier
// M approximates 10^(-f) * 2^(2*width)
template<typename T>
struct multiplier_t {
	std::array<T, 2> limbs; // {lower, upper}
};

// Modular inverse entry for fast divisibility-by-5^k testing
template<typename T>
struct minverse_t {
	T multiplier;
	T bound;
};

